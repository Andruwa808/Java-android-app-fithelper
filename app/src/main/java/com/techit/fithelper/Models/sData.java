package com.techit.fithelper.Models;

public class sData {
    private String calenda, ca, step;

    public sData() {
        this.calenda=calenda;
        this.ca=ca;
        this.step=step;
    }

    public String getCalenda() {
        return calenda;
    }

    public void setCalenda(String calenda) {
        this.calenda=calenda;
    }

    public String getCa() {
        return ca;
    }

    public void setCa(String ca) {
        this.ca=ca;
    }

    public String getStep() {
        return step;
    }

    public void setStep(String step) {
        this.step=step;
    }
}
